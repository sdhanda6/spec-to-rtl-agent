def render_verilog(ir: ModuleIR, strategy: RenderStrategy) -> str:
    lines: list[str] = []
    if strategy.include_header_comments:
        lines.append("// Generated by Spec-to-RTL pipeline: grammar -> IR -> RTL")
        for note in ir.notes:
            for note_line in str(note).splitlines():
                lines.append(f"// {note_line}")
        lines.append("")

    lines.append(f"module {ir.name} (")
    lines.extend(_render_port_block(ir, strategy))
    lines.append(");")
    lines.append("")
    lines.extend(_render_internal_declarations(ir, strategy))

    if ir.processes:
        lines.extend(_render_processes(ir))
    elif ir.design_kind == "counter" and ir.counter:
        lines.extend(_render_counter(ir, strategy))
    elif ir.design_kind == "register" and ir.register:
        lines.extend(_render_register(ir, strategy))
    elif ir.design_kind == "shift_register" and ir.shift_register:
        lines.extend(_render_shift_register(ir, strategy))
    elif ir.design_kind == "combinational" and ir.combinational:
        lines.extend(_render_combinational(ir, strategy))
    elif ir.design_kind == "fsm" and ir.fsm:
        lines.extend(_render_fsm(ir, strategy))
    else:
        lines.extend(_render_generic(ir, strategy))

    lines.append("endmodule")
    lines.append("")
    return "\n".join(lines)


def render_testbench(ir: ModuleIR) -> GeneratedTestbench:
    if not ir.verification.enable_tb:
        return GeneratedTestbench(text=None, evidence=VerificationEvidenceIR(tb_kind="none", achieved_level="none"))
    if ir.verification.seq_steps:
        return _render_seq_steps_tb(ir)
    if ir.verification.comb_vectors:
        return _render_comb_vectors_tb(ir)
    if ir.design_kind == "counter" and ir.counter:
        return GeneratedTestbench(
            text=_render_counter_tb(ir),
            evidence=VerificationEvidenceIR(
                tb_kind="counter_functional",
                achieved_level="functional",
                oracle_independent=True,
                covered_checks=["reset behavior", "count increment", "count hold"],
            ),
        )
    if ir.design_kind == "register" and ir.register:
        return GeneratedTestbench(
            text=_render_register_tb(ir),
            evidence=VerificationEvidenceIR(
                tb_kind="register_functional",
                achieved_level="functional",
                oracle_independent=True,
                covered_checks=["reset behavior", "single load", "hold when disabled"],
            ),
        )
    if ir.design_kind == "shift_register" and ir.shift_register:
        return GeneratedTestbench(
            text=_render_shift_register_tb(ir),
            evidence=VerificationEvidenceIR(
                tb_kind="shift_register_functional",
                achieved_level="functional",
                oracle_independent=True,
                covered_checks=["reset behavior", "shift behavior", "hold when disabled"],
            ),
        )
    if ir.design_kind == "fsm" and ir.fsm:
        return _render_fsm_tb(ir)
    if ir.design_kind == "combinational" and ir.combinational:
        return _render_combinational_tb(ir)
    return GeneratedTestbench(
        text=_render_smoke_tb(ir),
        evidence=VerificationEvidenceIR(
            tb_kind="generic_smoke",
            achieved_level="smoke",
            oracle_independent=False,
            limitations=["no independent functional oracle for this design kind"],
        ),
    )


def _render_port_block(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    items: list[str] = []
    for idx, port in enumerate(ir.ports):
        suffix = "," if idx < len(ir.ports) - 1 else ""
        items.append(f"    {_port_decl(port, ir, strategy)}{suffix}")
    return items


def _render_internal_declarations(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    lines: list[str] = []
    for signal in ir.signals:
        lines.append(f"    {signal.kind} {_verilog_range(signal.width)}{signal.name};")
    if strategy.internalize_outputs:
        for port in ir.ports:
            if port.direction == "output":
                lines.append(f"    reg {_verilog_range(port.width)}{port.name}_r;")
                lines.append(f"    assign {port.name} = {port.name}_r;")
    if lines:
        lines.append("")
    return lines


def _render_processes(ir: ModuleIR) -> list[str]:
    lines: list[str] = []
    for process in ir.processes:
        if process.kind == "comb":
            lines.append("    always @* begin")
            lines.extend(_render_statements(ir, process.body, indent="        ", blocking=True))
            lines.append("    end")
            lines.append("")
            continue

        clk = process.clock or ir.clock or "clk"
        reset = process.reset or ir.reset
        if reset:
            sensitivity = f"posedge {clk} or {'negedge' if reset.active_low else 'posedge'} {reset.signal}"
            cond = f"!{reset.signal}" if reset.active_low else reset.signal
            lines.append(f"    always @({sensitivity}) begin")
            lines.append(f"        if ({cond}) begin")
            lines.extend(_render_statements(ir, process.reset_body, indent="            ", blocking=False))
            lines.append("        end else begin")
            lines.extend(_render_statements(ir, process.body, indent="            ", blocking=False))
            lines.append("        end")
            lines.append("    end")
            lines.append("")
        else:
            lines.append(f"    always @(posedge {clk}) begin")
            lines.extend(_render_statements(ir, process.body, indent="        ", blocking=False))
            lines.append("    end")
            lines.append("")
    return lines


def _render_statements(ir: ModuleIR, statements: list[AssignmentIR | IfIR], indent: str, blocking: bool) -> list[str]:
    lines: list[str] = []
    for statement in statements:
        if isinstance(statement, AssignmentIR):
            op = "=" if blocking or statement.blocking else "<="
            lines.append(f"{indent}{statement.target} {op} {_forced_expr(ir, statement.target, statement.expr)};")
            continue
        lines.append(f"{indent}if ({render_expr(statement.condition)}) begin")
        lines.extend(_render_statements(ir, statement.then_body, indent + "    ", blocking))
        if statement.else_body:
            lines.append(f"{indent}end else begin")
            lines.extend(_render_statements(ir, statement.else_body, indent + "    ", blocking))
        lines.append(f"{indent}end")
    return lines


def _render_counter(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    counter = ir.counter
    assert counter is not None
    target = _target_name(counter.target, strategy)
    width = _signal_width(ir, counter.target)
    step = _literal(width, counter.step)
    max_value = _literal(width, (1 << width) - 1)
    reset_value = _literal(width, _state_reset(ir, counter.target))
    lines = _seq_header(ir)
    lines.append(f"            {target} <= {reset_value};")
    lines.append("        end else begin")
    if counter.enable:
        lines.append(f"            if ({counter.enable}) begin")
        if ir.repair_controls.counter_hold_when_enabled:
            lines.append(f"                {target} <= {target};")
        elif counter.direction == "down":
            if counter.wrap:
                lines.append(f"                {target} <= {target} - {step};")
            else:
                lines.append(f"                if ({target} != {_literal(width, 0)}) {target} <= {target} - {step};")
        else:
            if counter.wrap:
                lines.append(f"                {target} <= {target} + {step};")
            else:
                lines.append(f"                if ({target} != {max_value}) {target} <= {target} + {step};")
        lines.append("            end")
        if ir.repair_controls.counter_ignore_enable:
            op = "-" if counter.direction == "down" else "+"
            lines.append("            else begin")
            lines.append(f"                {target} <= {target} {op} {step};")
            lines.append("            end")
    else:
        op = "-" if counter.direction == "down" else "+"
        lines.append(f"            {target} <= {target} {op} {step};")
    lines.append("        end")
    lines.append("    end")
    lines.append("")
    return lines


def _render_register(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    behavior = ir.register
    assert behavior is not None
    target = _target_name(behavior.target, strategy)
    reset_value = _literal(_signal_width(ir, behavior.target), _state_reset(ir, behavior.target))
    lines = _seq_header(ir)
    lines.append(f"            {target} <= {reset_value};")
    lines.append("        end else begin")
    if behavior.enable:
        lines.append(f"            if ({behavior.enable}) begin")
        if ir.repair_controls.register_hold_when_enabled:
            lines.append(f"                {target} <= {target};")
        else:
            lines.append(f"                {target} <= {behavior.data};")
        lines.append("            end")
        if ir.repair_controls.register_ignore_enable:
            lines.append("            else begin")
            lines.append(f"                {target} <= {behavior.data};")
            lines.append("            end")
        elif behavior.hold_value:
            lines.append("            else begin")
            lines.append(f"                {target} <= {behavior.hold_value};")
            lines.append("            end")
    else:
        lines.append(f"            {target} <= {behavior.data};")
    lines.append("        end")
    lines.append("    end")
    lines.append("")
    return lines


def _render_shift_register(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    behavior = ir.shift_register
    assert behavior is not None
    target = _target_name(behavior.target, strategy)
    width = _signal_width(ir, behavior.target)
    reset_value = _literal(width, _state_reset(ir, behavior.target))
    lines = _seq_header(ir)
    lines.append(f"            {target} <= {reset_value};")
    lines.append("        end else begin")
    effective_direction = behavior.direction
    if ir.repair_controls.shift_reverse_direction:
        effective_direction = "left" if behavior.direction == "right" else "right"
    if behavior.enable:
        lines.append(f"            if ({behavior.enable}) begin")
    if effective_direction == "right":
        lines.append(f"                {target} <= {{{behavior.serial_in}, {target}[{width - 1}:1]}};")
    elif width > 1:
        lines.append(f"                {target} <= {{{target}[{width - 2}:0], {behavior.serial_in}}};")
    else:
        lines.append(f"                {target} <= {behavior.serial_in};")
    if behavior.serial_out:
        lines.append(f"                {behavior.serial_out} <= {target}[{0 if effective_direction == 'right' else width - 1}];")
    if behavior.enable:
        lines.append("            end")
        if ir.repair_controls.shift_ignore_enable:
            lines.append("            else begin")
            if effective_direction == "right":
                lines.append(f"                {target} <= {{{behavior.serial_in}, {target}[{width - 1}:1]}};")
            elif width > 1:
                lines.append(f"                {target} <= {{{target}[{width - 2}:0], {behavior.serial_in}}};")
            else:
                lines.append(f"                {target} <= {behavior.serial_in};")
            lines.append("            end")
    lines.append("        end")
    lines.append("    end")
    lines.append("")
    return lines


def _render_combinational(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    lines = ["    always @* begin"]
    for operation in ir.combinational:
        target = _target_name(operation.target, strategy)
        expr = _literal(_signal_width(ir, operation.target), 0) if _force_zero(ir, operation.target) else _comb_expr(operation)
        lines.append(f"        {target} = {expr};")
    lines.append("    end")
    lines.append("")
    return lines


def _render_fsm(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    fsm = ir.fsm
    assert fsm is not None
    width = max(1, (len(fsm.states) - 1).bit_length())
    lines: list[str] = []
    for index, state in enumerate(fsm.states):
        lines.append(f"    localparam [{width - 1}:0] {state} = {width}'d{index};")
    lines.append(f"    reg [{width - 1}:0] {fsm.state_signal};")
    lines.append(f"    reg [{width - 1}:0] next_{fsm.state_signal};")
    lines.append("")
    lines.extend(_seq_header(ir, state_signal=fsm.state_signal, reset_state=fsm.reset_state))
    lines.append("        end else begin")
    lines.append(f"            {fsm.state_signal} <= next_{fsm.state_signal};")
    lines.append("        end")
    lines.append("    end")
    lines.append("")
    lines.append("    always @* begin")
    lines.append(f"        next_{fsm.state_signal} = {fsm.state_signal};")
    for port in ir.ports:
        if port.direction == "output":
            lines.append(f"        {_target_name(port.name, strategy)} = {_literal(port.width, 0)};")
    lines.append(f"        case ({fsm.state_signal})")
    for state in fsm.states:
        lines.append(f"            {state}: begin")
        output_behavior = next((item for item in fsm.outputs if item.state == state), None)
        if output_behavior:
            for target, expr in output_behavior.assignments.items():
                rendered_expr = _literal(_signal_width(ir, target), 0) if _force_zero(ir, target) else expr
                lines.append(f"                {_target_name(target, strategy)} = {rendered_expr};")
        transitions = [item for item in fsm.transitions if item.src == state]
        if transitions:
            for transition in transitions:
                if ir.repair_controls.fsm_force_self_loop:
                    lines.append(f"                next_{fsm.state_signal} = {fsm.state_signal};")
                elif transition.condition:
                    lines.append(f"                if ({transition.condition}) next_{fsm.state_signal} = {transition.dst};")
                else:
                    lines.append(f"                next_{fsm.state_signal} = {transition.dst};")
        else:
            lines.append(f"                next_{fsm.state_signal} = {fsm.state_signal};")
        lines.append("            end")
    lines.append("            default: begin")
    lines.append(f"                next_{fsm.state_signal} = {fsm.reset_state};")
    lines.append("            end")
    lines.append("        endcase")
    lines.append("    end")
    lines.append("")
    return lines


def _render_generic(ir: ModuleIR, strategy: RenderStrategy) -> list[str]:
    if ir.clock:
        lines = _seq_header(ir)
        for port in ir.ports:
            if port.direction == "output":
                lines.append(f"            {_target_name(port.name, strategy)} <= {_forced_or_default_literal(ir, port.name, port.width)};")
        lines.append("        end else begin")
        lines.append("            // TODO: lower richer grammar rules into sequential behavior.")
        lines.append("        end")
        lines.append("    end")
        lines.append("")
        return lines
    lines = ["    always @* begin"]
    for port in ir.ports:
        if port.direction == "output":
            lines.append(f"        {_target_name(port.name, strategy)} = {_forced_or_default_literal(ir, port.name, port.width)};")
    lines.append("    end")
    lines.append("")
    return lines


def _seq_header(ir: ModuleIR, state_signal: str | None = None, reset_state: str | None = None) -> list[str]:
    clk = ir.clock or "clk"
    if ir.reset:
        sensitivity = f"posedge {clk} or {'negedge' if ir.reset.active_low else 'posedge'} {ir.reset.signal}"
        cond = f"!{ir.reset.signal}" if ir.reset.active_low else ir.reset.signal
        lines = [f"    always @({sensitivity}) begin", f"        if ({cond}) begin"]
        if state_signal and reset_state:
            lines.append(f"            {state_signal} <= {reset_state};")
        return lines
    return [f"    always @(posedge {clk}) begin", "        begin"]


def _render_counter_tb(ir: ModuleIR) -> str:
    counter = ir.counter
    assert counter is not None
    width = _signal_width(ir, counter.target)
    clk = ir.clock or "clk"
    rst = ir.reset.signal if ir.reset else "rst"
    rst_asserted = "1'b0" if ir.reset and ir.reset.active_low else "1'b1"
    rst_deasserted = "1'b1" if ir.reset and ir.reset.active_low else "1'b0"
    lines = ["`timescale 1ns/1ps", "", f"module tb_{ir.name};"]
    for port in ir.ports:
        decl = "reg" if port.direction == "input" else "wire"
        lines.append(f"    {decl} {_verilog_range(port.width)}{port.name};")
    lines.extend(
        [
            "",
            f"    {ir.name} dut (",
            ",\n".join(f"        .{port.name}({port.name})" for port in ir.ports),
            "    );",
            "",
            f"    always #5 {clk} = ~{clk};",
            "",
            "    initial begin",
            f"        {clk} = 1'b0;",
            f"        {rst} = {rst_asserted};",
        ]
    )
    if counter.enable:
        lines.append(f"        {counter.enable} = 1'b0;")
    lines.extend(
        [
            "        #2;",
            f"        if ({counter.target} !== {_literal(width, 0)}) $fatal(1, \"reset failed\");",
            f"        {rst} = {rst_deasserted};",
        ]
    )
    if counter.enable:
        lines.append(f"        {counter.enable} = 1'b1;")
    lines.extend(
        [
            f"        repeat (3) @(posedge {clk});",
            f"        #1 if ({counter.target} !== {_literal(width, 3)}) $fatal(1, \"increment failed\");",
        ]
    )
    if counter.enable:
        lines.extend(
            [
                f"        {counter.enable} = 1'b0;",
                f"        @(posedge {clk});",
                f"        #1 if ({counter.target} !== {_literal(width, 3)}) $fatal(1, \"hold failed\");",
            ]
        )
    lines.extend([f"        $display(\"PASS tb_{ir.name}\");", "        $finish;", "    end", "endmodule", ""])
    return "\n".join(lines)


