def _render_seq_steps_tb(ir: ModuleIR) -> GeneratedTestbench:
    lines = ["`timescale 1ns/1ps", "", f"module tb_{ir.name};"]
    inputs = [port for port in ir.ports if port.direction == "input"]
    outputs = [port for port in ir.ports if port.direction == "output"]
    clk = ir.clock or next((port.name for port in inputs if port.name == "clk"), None)
    for port in inputs:
        lines.append(f"    reg {_verilog_range(port.width)}{port.name};")
    for port in outputs:
        lines.append(f"    wire {_verilog_range(port.width)}{port.name};")
        lines.append(f"    reg {_verilog_range(port.width)}expected_{port.name};")
    lines.extend(["", f"    {ir.name} dut (", ",\n".join(f"        .{port.name}({port.name})" for port in ir.ports), "    );", ""])
    if clk:
        lines.extend([f"    always #5 {clk} = ~{clk};", ""])
    lines.extend(["    task check_outputs;", "        input [255:0] step_name;", "        begin"])
    for port in outputs:
        lines.append(f"            if ({port.name} !== expected_{port.name}) begin")
        lines.append(f"                $display(\"FAIL %0s: expected {port.name}=%b got %b\", step_name, expected_{port.name}, {port.name});")
        lines.append("                $fatal(1);")
        lines.append("            end")
    lines.extend(["        end", "    endtask", "", "    initial begin"])
    for port in inputs:
        lines.append(f"        {port.name} = {_literal(port.width, 0)};")
    for step in ir.verification.seq_steps:
        for name, expr in step.drive.items():
            lines.append(f"        {name} = {render_expr(expr)};")
        if step.delay is not None:
            lines.append(f"        #{step.delay};")
        elif step.edge:
            lines.append(f"        @({step.edge});")
        else:
            lines.append("        #1;")
        for target, expr in step.expect.items():
            lines.append(f"        expected_{target} = {render_expr(expr)};")
        if step.expect:
            lines.append(f"        #1 check_outputs(\"{step.name}\");")
    lines.extend([f"        $display(\"PASS tb_{ir.name}\");", "        $finish;", "    end", "endmodule", ""])
    return GeneratedTestbench(
        text="\n".join(lines),
        evidence=VerificationEvidenceIR(
            tb_kind="verification_steps_functional",
            achieved_level="functional",
            oracle_independent=True,
            covered_checks=[f"user sequence step {item.name}" for item in ir.verification.seq_steps],
        ),
    )


def _emit_expected_output_assignments(lines: list[str], outputs: list[PortIR], assignments: dict[str, str]) -> None:
    for output in outputs:
        lines.append(f"        expected_{output.name} = {assignments.get(output.name, _literal(output.width, 0))};")


def _fsm_output_for_state(outputs: list[FSMOutputIR], state: str) -> dict[str, str]:
    item = next((entry for entry in outputs if entry.state == state), None)
    return item.assignments if item else {}


def _comb_expr(operation: CombBehaviorIR) -> str:
    if operation.operator == "buf":
        return operation.operands[0]
    if operation.operator == "not":
        return f"~{operation.operands[0]}"
    if operation.operator == "and":
        return " & ".join(operation.operands)
    if operation.operator == "or":
        return " | ".join(operation.operands)
    if operation.operator == "xor":
        return " ^ ".join(operation.operands)
    if operation.operator == "add":
        return " + ".join(operation.operands)
    if operation.operator == "sub":
        return " - ".join(operation.operands)
    if operation.operator == "mux":
        false_value = operation.operands[0]
        true_value = operation.operands[1] if len(operation.operands) > 1 else operation.operands[0]
        return f"{operation.select} ? {true_value} : {false_value}"
    if operation.operator == "eq":
        return " == ".join(operation.operands)
    if operation.operator == "lt":
        return " < ".join(operation.operands)
    if operation.operator == "gt":
        return " > ".join(operation.operands)
    if operation.operator == "expr" and operation.expr is not None:
        return render_expr(operation.expr)
    return "1'b0"


def _comb_expected_assignments(operations: list[CombBehaviorIR]) -> list[str]:
    lines: list[str] = []
    for operation in operations:
        target = f"expected_{operation.target}"
        if operation.operator == "buf":
            lines.append(f"        {target} = {operation.operands[0]};")
        elif operation.operator == "not":
            lines.append(f"        {target} = ~{operation.operands[0]};")
        elif operation.operator == "and":
            lines.append(f"        {target} = {' & '.join(operation.operands)};")
        elif operation.operator == "or":
            lines.append(f"        {target} = {' | '.join(operation.operands)};")
        elif operation.operator == "xor":
            lines.append(f"        {target} = {' ^ '.join(operation.operands)};")
        elif operation.operator == "add":
            lines.append(f"        {target} = {' + '.join(operation.operands)};")
        elif operation.operator == "sub":
            lines.append(f"        {target} = {' - '.join(operation.operands)};")
        elif operation.operator == "mux":
            false_value = operation.operands[0]
            true_value = operation.operands[1] if len(operation.operands) > 1 else operation.operands[0]
            lines.append(f"        if ({operation.select}) {target} = {true_value}; else {target} = {false_value};")
        elif operation.operator == "eq":
            lines.append(f"        {target} = ({operation.operands[0]} == {operation.operands[1]});")
        elif operation.operator == "lt":
            lines.append(f"        {target} = ({operation.operands[0]} < {operation.operands[1]});")
        elif operation.operator == "gt":
            lines.append(f"        {target} = ({operation.operands[0]} > {operation.operands[1]});")
    return lines


def _oracle_supported(operation: CombBehaviorIR) -> bool:
    return operation.operator in {"buf", "not", "and", "or", "xor", "add", "sub", "mux", "eq", "lt", "gt"}


def _is_exhaustive_boolean(ir: ModuleIR) -> bool:
    inputs = [port for port in ir.ports if port.direction == "input"]
    return len(inputs) <= 4 and all(port.width == 1 for port in inputs)


def _combinational_vectors(ir: ModuleIR) -> list[tuple[str, dict[str, str]]]:
    inputs = [port for port in ir.ports if port.direction == "input"]
    if _is_exhaustive_boolean(ir):
        vectors = []
        for value in range(1 << len(inputs)):
            assignments: dict[str, str] = {}
            for index, port in enumerate(inputs):
                bit = (value >> (len(inputs) - index - 1)) & 1
                assignments[port.name] = f"1'b{bit}"
            vectors.append((f"vector_{value}", assignments))
        return vectors
    vectors = []
    zero = {port.name: _literal(port.width, 0) for port in inputs}
    vectors.append(("zeros", zero))
    ones = {port.name: _literal(port.width, (1 << port.width) - 1) for port in inputs}
    vectors.append(("ones", ones))
    for port in inputs:
        assignments = {item.name: _literal(item.width, 0) for item in inputs}
        assignments[port.name] = _literal(port.width, 1)
        vectors.append((f"{port.name}_hot", assignments))
    return vectors


def _port_decl(port: PortIR, ir: ModuleIR, strategy: RenderStrategy) -> str:
    width = _verilog_range(port.width)
    if port.direction == "output" and not strategy.internalize_outputs:
        return f"output reg {width}{port.name}".rstrip()
    return f"{port.direction} {width}{port.name}".rstrip()


def _verilog_range(width: int) -> str:
    return f"[{width - 1}:0] " if width > 1 else ""


def _target_name(name: str, strategy: RenderStrategy) -> str:
    return f"{name}_r" if strategy.internalize_outputs else name


def _signal_width(ir: ModuleIR, name: str) -> int:
    for signal in ir.signals:
        if signal.name == name:
            return signal.width
    for signal in ir.state:
        if signal.name == name:
            return signal.width
    for port in ir.ports:
        if port.name == name:
            return port.width
    return 1


def _state_reset(ir: ModuleIR, name: str) -> int:
    for signal in ir.state:
        if signal.name == name:
            return signal.reset_value
    return 0


def _literal(width: int, value: int) -> str:
    return f"{width}'d{value}"


def _force_zero(ir: ModuleIR, target: str) -> bool:
    return target in ir.repair_controls.zero_output_targets


def _forced_or_default_literal(ir: ModuleIR, target: str, width: int) -> str:
    if _force_zero(ir, target):
        return _literal(width, 0)
    return _literal(width, 0)


def _forced_expr(ir: ModuleIR, target: str, expr: object) -> str:
    if _force_zero(ir, target):
        return _literal(_signal_width(ir, target), 0)
    return render_expr(expr)  # type: ignore[arg-type]
