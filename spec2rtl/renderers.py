def _render_shift_register_tb(ir: ModuleIR) -> str:
    behavior = ir.shift_register
    assert behavior is not None
    width = _signal_width(ir, behavior.target)
    clk = ir.clock or "clk"
    rst = ir.reset.signal if ir.reset else "rst"
    rst_asserted = "1'b0" if ir.reset and ir.reset.active_low else "1'b1"
    rst_deasserted = "1'b1" if ir.reset and ir.reset.active_low else "1'b0"
    first_value = _literal(width, 1 if behavior.direction == "left" else 1 << max(width - 1, 0))
    second_value = _literal(width, 2 if behavior.direction == "left" and width > 1 else (1 << max(width - 2, 0) if behavior.direction == "right" and width > 1 else 0))
    lines = ["`timescale 1ns/1ps", "", f"module tb_{ir.name};"]
    for port in ir.ports:
        decl = "reg" if port.direction == "input" else "wire"
        lines.append(f"    {decl} {_verilog_range(port.width)}{port.name};")
    lines.extend(
        [
            "",
            f"    {ir.name} dut (",
            ",\n".join(f"        .{port.name}({port.name})" for port in ir.ports),
            "    );",
            "",
            f"    always #5 {clk} = ~{clk};",
            "",
            "    initial begin",
            f"        {clk} = 1'b0;",
            f"        {rst} = {rst_asserted};",
            f"        {behavior.serial_in} = 1'b0;",
        ]
    )
    if behavior.enable:
        lines.append(f"        {behavior.enable} = 1'b0;")
    lines.extend(
        [
            "        #2;",
            f"        if ({behavior.target} !== {_literal(width, 0)}) $fatal(1, \"reset failed\");",
            f"        {rst} = {rst_deasserted};",
        ]
    )
    if behavior.enable:
        lines.append(f"        {behavior.enable} = 1'b1;")
    lines.extend(
        [
            f"        {behavior.serial_in} = 1'b1;",
            f"        @(posedge {clk});",
            f"        #1 if ({behavior.target} !== {first_value}) $fatal(1, \"shift failed\");",
            f"        {behavior.serial_in} = 1'b0;",
            f"        @(posedge {clk});",
            f"        #1 if ({behavior.target} !== {second_value}) $fatal(1, \"shift failed\");",
        ]
    )
    if behavior.enable:
        lines.extend(
            [
                f"        {behavior.enable} = 1'b0;",
                f"        {behavior.serial_in} = 1'b1;",
                f"        @(posedge {clk});",
                f"        #1 if ({behavior.target} !== {second_value}) $fatal(1, \"hold failed\");",
            ]
        )
    lines.extend([f"        $display(\"PASS tb_{ir.name}\");", "        $finish;", "    end", "endmodule", ""])
    return "\n".join(lines)


def _render_smoke_tb(ir: ModuleIR) -> str:
    lines = ["`timescale 1ns/1ps", "", f"module tb_{ir.name};"]
    for port in ir.ports:
        decl = "reg" if port.direction == "input" else "wire"
        lines.append(f"    {decl} {_verilog_range(port.width)}{port.name};")
    lines.extend(
        [
            "",
            f"    {ir.name} dut (",
            ",\n".join(f"        .{port.name}({port.name})" for port in ir.ports),
            "    );",
            "",
            "    initial begin",
        ]
    )
    for port in ir.ports:
        if port.direction == "input":
            lines.append(f"        {port.name} = {_literal(port.width, 0)};")
    lines.extend(["        #10;", f"        $display(\"PASS tb_{ir.name}\");", "        $finish;", "    end", "endmodule", ""])
    return "\n".join(lines)


def _render_fsm_tb(ir: ModuleIR) -> GeneratedTestbench:
    fsm = ir.fsm
    assert fsm is not None
    output_ports = [port for port in ir.ports if port.direction == "output"]
    unsupported = [item.condition for item in fsm.transitions if item.condition and item.condition not in {port.name for port in ir.ports if port.direction == "input"}]
    if unsupported:
        return GeneratedTestbench(
            text=_render_smoke_tb(ir),
            evidence=VerificationEvidenceIR(
                tb_kind="fsm_smoke",
                achieved_level="smoke",
                oracle_independent=True,
                limitations=[f"unsupported FSM transition conditions for directed checking: {', '.join(unsupported)}"],
            ),
        )

    clk = ir.clock or "clk"
    rst = ir.reset.signal if ir.reset else "rst"
    rst_asserted = "1'b0" if ir.reset and ir.reset.active_low else "1'b1"
    rst_deasserted = "1'b1" if ir.reset and ir.reset.active_low else "1'b0"
    state_width = max(1, (len(fsm.states) - 1).bit_length())
    lines = ["`timescale 1ns/1ps", "", f"module tb_{ir.name};"]
    for port in ir.ports:
        decl = "reg" if port.direction == "input" else "wire"
        lines.append(f"    {decl} {_verilog_range(port.width)}{port.name};")
    for index, state in enumerate(fsm.states):
        lines.append(f"    localparam [{state_width - 1}:0] {state} = {state_width}'d{index};")
    lines.extend(["", f"    {ir.name} dut (", ",\n".join(f"        .{port.name}({port.name})" for port in ir.ports), "    );", "", f"    always #5 {clk} = ~{clk};", ""])
    for output in output_ports:
        lines.append(f"    reg {_verilog_range(output.width)}expected_{output.name};")
    lines.extend(
        [
            "    task check_state_outputs;",
            "        input [255:0] step_name;",
            "        input integer expected_state;",
            "        begin",
            f"            if (dut.{fsm.state_signal} !== expected_state) begin",
            f"                $display(\"FAIL %0s: expected state=%0d got=%0d\", step_name, expected_state, dut.{fsm.state_signal});",
            "                $fatal(1);",
            "            end",
        ]
    )
    for output in output_ports:
        lines.append(f"            if ({output.name} !== expected_{output.name}) begin")
        lines.append(f"                $display(\"FAIL %0s: expected {output.name}=%b got %b\", step_name, expected_{output.name}, {output.name});")
        lines.append("                $fatal(1);")
        lines.append("            end")
    lines.extend(["        end", "    endtask", ""])

    lines.extend(["    initial begin", f"        {clk} = 1'b0;", f"        {rst} = {rst_asserted};"])
    for port in ir.ports:
        if port.direction == "input" and port.name not in {clk, rst}:
            lines.append(f"        {port.name} = {_literal(port.width, 0)};")
    _emit_expected_output_assignments(lines, output_ports, _fsm_output_for_state(fsm.outputs, fsm.reset_state))
    lines.extend(["        #1;", f"        check_state_outputs(\"reset\", {fsm.reset_state});", f"        {rst} = {rst_deasserted};"])

    covered = ["reset behavior"]
    for transition in fsm.transitions:
        state_label = transition.src.lower()
        if transition.condition and transition.expect_hold_when_false:
            lines.append(f"        {transition.condition} = 1'b0;")
            _emit_expected_output_assignments(lines, output_ports, _fsm_output_for_state(fsm.outputs, transition.src))
            lines.append(f"        @(posedge {clk});")
            lines.append(f"        #1 check_state_outputs(\"hold_{state_label}\", {transition.src});")
            covered.append(f"hold in {transition.src} when {transition.condition}=0")
        if transition.condition:
            lines.append(f"        {transition.condition} = 1'b1;")
        _emit_expected_output_assignments(lines, output_ports, _fsm_output_for_state(fsm.outputs, transition.dst))
        lines.append(f"        @(posedge {clk});")
        lines.append(f"        #1 check_state_outputs(\"transition_{transition.src}_to_{transition.dst}\", {transition.dst});")
        covered.append(f"transition {transition.src}->{transition.dst}")
        if transition.condition:
            lines.append(f"        {transition.condition} = 1'b0;")

    lines.extend([f"        $display(\"PASS tb_{ir.name}\");", "        $finish;", "    end", "endmodule", ""])
    return GeneratedTestbench(
        text="\n".join(lines),
        evidence=VerificationEvidenceIR(
            tb_kind="fsm_functional",
            achieved_level="functional",
            oracle_independent=True,
            covered_checks=covered + [f"outputs in state {state.state}" for state in fsm.outputs],
        ),
    )


def _render_combinational_tb(ir: ModuleIR) -> GeneratedTestbench:
    if not all(_oracle_supported(operation) for operation in ir.combinational):
        return GeneratedTestbench(
            text=_render_smoke_tb(ir),
            evidence=VerificationEvidenceIR(
                tb_kind="combinational_smoke",
                achieved_level="smoke",
                oracle_independent=False,
                limitations=["no independent oracle available for one or more combinational operations"],
            ),
        )
    lines = ["`timescale 1ns/1ps", "", f"module tb_{ir.name};"]
    inputs = [port for port in ir.ports if port.direction == "input"]
    outputs = [port for port in ir.ports if port.direction == "output"]
    for port in inputs:
        lines.append(f"    reg {_verilog_range(port.width)}{port.name};")
    for port in outputs:
        lines.append(f"    wire {_verilog_range(port.width)}{port.name};")
        lines.append(f"    reg {_verilog_range(port.width)}expected_{port.name};")
    lines.extend(["", f"    {ir.name} dut (", ",\n".join(f"        .{port.name}({port.name})" for port in ir.ports), "    );", "", "    task check_outputs;", "        input [255:0] vector_name;", "        begin"])
    for port in outputs:
        lines.append(f"            if ({port.name} !== expected_{port.name}) begin")
        lines.append(f"                $display(\"FAIL %0s: expected {port.name}=%b got %b\", vector_name, expected_{port.name}, {port.name});")
        lines.append("                $fatal(1);")
        lines.append("            end")
    lines.extend(["        end", "    endtask", "", "    initial begin"])
    for port in inputs:
        lines.append(f"        {port.name} = {_literal(port.width, 0)};")

    for vector_name, assignments in _combinational_vectors(ir):
        for name, value in assignments.items():
            lines.append(f"        {name} = {value};")
        lines.extend(_comb_expected_assignments(ir.combinational))
        lines.append(f"        #1 check_outputs(\"{vector_name}\");")
    lines.extend([f"        $display(\"PASS tb_{ir.name}\");", "        $finish;", "    end", "endmodule", ""])
    vector_note = "exhaustive truth-table vectors" if _is_exhaustive_boolean(ir) else "directed input vectors"
    return GeneratedTestbench(
        text="\n".join(lines),
        evidence=VerificationEvidenceIR(
            tb_kind="combinational_functional",
            achieved_level="functional",
            oracle_independent=True,
            covered_checks=[vector_note, "independent oracle expected-value checks"],
        ),
    )


